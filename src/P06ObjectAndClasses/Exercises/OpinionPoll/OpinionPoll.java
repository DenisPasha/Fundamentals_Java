package P06ObjectAndClasses.Exercises.OpinionPoll;

public class OpinionPoll {

    String name;
    int age;

    public OpinionPoll(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
